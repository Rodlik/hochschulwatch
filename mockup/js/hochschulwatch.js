$(function() {

	$('input.hochschulsuche').on('propertychange keyup input paste change', function() {
		var resultSelector = $(this).attr('data-result');

		if ($(this).val() == '') {
			// input empty
			$(resultSelector).hide();
			return;
		}

		var f = new Fuse(hochschulen, {
			keys: ['name'],
			threshold: 0.4
		});

		var result = f.search( $(this).val() );
		$(resultSelector).empty().fadeIn();
		$(result).each(function(i, hochschule) {
			if (i > 10) return;
			$(resultSelector).append(
				'<li>'+
				'<a href="#">'+
				hochschule.name+
				'</a>'+
				'</li>'
			);
		});

		if (result.length == 0) {
			$(resultSelector).append(
				'<li>'+
				'<em>'+
				'nichts gefunden'+
				'</em>'+
				'</li>'
			);
		}

	}).blur(function() {
		var resultSelector = $(this).attr('data-result');
		$(resultSelector).hide();
	}). trigger('blur');
});
